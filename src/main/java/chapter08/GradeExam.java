package chapter08;

public class GradeExam {

    public static void main(String[] args) {
        char[][] answers = {
                {'A', 'B', 'A', 'C', 'C', 'D', 'E', 'E', 'A', 'D'},
                {'D', 'B', 'A', 'B', 'C', 'A', 'E', 'E', 'A', 'D'},
                {'E', 'D', 'D', 'A', 'C', 'B', 'E', 'E', 'A', 'D'},
                {'C', 'B', 'A', 'E', 'D', 'C', 'E', 'E', 'A', 'D'},
                {'A', 'B', 'D', 'C', 'C', 'D', 'E', 'E', 'A', 'D'},
                {'B', 'B', 'E', 'C', 'C', 'D', 'E', 'E', 'A', 'D'},
                {'B', 'B', 'A', 'C', 'C', 'D', 'E', 'E', 'A', 'D'},
                {'E', 'B', 'E', 'C', 'C', 'D', 'E', 'E', 'A', 'D'}};

        char[] keys = {'D', 'B', 'D', 'C', 'C', 'D', 'A', 'E', 'A', 'D'};

        for (int stud = 0; stud < answers.length; stud++){
            int currentStudentScore = 0;
            for (int an = 0; an < answers[stud].length; an ++) {
                int currentAnswer = answers[stud][an];
                int correctAnswer = keys[an];
                if (currentAnswer == correctAnswer) {
                    currentStudentScore ++;
                }
            }
            System.out.printf("The score for student %d is %d\n", stud, currentStudentScore);
        }
    }
}
